const fs = require('fs');
const path = require('path');

function main() {
  const address = process.argv[2];
  if (!address || !address.startsWith('0x') || address.length !== 42) {
    console.log('Usage: node scripts/export-frontend.cjs <FACTORY_ADDRESS>');
    process.exit(1);
  }

  const artifactPath = path.join(
    __dirname,
    '..',
    'artifacts',
    'contracts',
    'NeonLaunchFactoryMock.sol',
    'NeonLaunchFactoryMock.json'
  );

  const artifact = JSON.parse(fs.readFileSync(artifactPath, 'utf8'));
  const abi = artifact.abi;

  const outDir = path.join(__dirname, '..', 'src', 'generated');
  fs.mkdirSync(outDir, { recursive: true });

  const outFile = path.join(outDir, 'factory.js');
  const content = `// Auto-generated by export-frontend.cjs\n\nexport const FACTORY_ADDRESS = '${address}';\nexport const FACTORY_ABI = ${JSON.stringify(abi, null, 2)};\n`;

  fs.writeFileSync(outFile, content, 'utf8');
  console.log('✅ Wrote', outFile);
}

main();